                    FocusIndicator(position: focusPoint)
                        .transition(.opacity)
                        .animation(.easeInOut(duration: 0.2), value: showFocusIndicator)
                }
                
                // Bottom controls
                VStack {
                    Spacer()
                    
                    HStack {
                        // Shutter button (centered)
