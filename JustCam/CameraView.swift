                                        .foregroundColor(cameraModel.currentZoom == zoom ? .yellow : .white)
                                }
                            }
                        }
                        .padding(.horizontal, 20)
                    }
                    .padding(.bottom, 30)
                    .background(
                        Rectangle()
                            .fill(Color.black.opacity(0.4))
                            .frame(height: 90)
                            .edgesIgnoringSafeArea(.bottom)
                    )
                }
                
                // Âè≥‰∏äËßíÊéßÂà∂ÊåâÈíÆÔºöÁøªËΩ¨Áõ∏Êú∫ÂíåÈó™ÂÖâÁÅØ
