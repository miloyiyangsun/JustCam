                    }
                }
            }
        }
    }
    
    private func openPhotoLibrary() {
        let photosURL = URL(string: "photos-redirect://") ?? URL(string: "photos://") ?? URL(string: "mobilephotos://")
        
        if let url = photosURL, UIApplication.shared.canOpenURL(url) {
            UIApplication.shared.open(url, options: [:]) { success in
                if success {
                    CameraLog.log("‚úÖ Successfully opened photo library")
                } else {
                    CameraLog.error("‚ùå Failed to open photo library")
                }
            }
        } else {
            CameraLog.error("‚ùå Cannot open photo library URL")
        }
    }
    
    private func triggerFlashAnimation() {
        CameraLog.log("‚ú® Triggering flash animation")
        
        withAnimation(.easeInOut(duration: 0.05)) {
            showFlashAnimation = true
        }
        
        DispatchQueue.main.asyncAfter(deadline: .now() + 0.1) {
            withAnimation(.easeInOut(duration: 0.05)) {
                showFlashAnimation = false
            }
            CameraLog.log("‚ú® Flash animation completed")
        }
    }
}

