                        // ÁõëÂê¨ÁÖßÁâá‰øùÂ≠òÂÆåÊàêÈÄöÁü•
                        NotificationCenter.default.addObserver(
                            forName: Notification.Name("PhotoSaved"),
                            object: nil,
                            queue: .main
                        ) { _ in
                            isCapturing = false
                        }
                    }
                
                // Grid overlay
                if showGrid {
                    GridOverlay()
                }
                
