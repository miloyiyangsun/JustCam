                                .font(.system(size: 20))
                                .foregroundColor(.white)
                                .padding(10)
                                .background(Color.black.opacity(0.4))
                                .cornerRadius(8)
                        }
                        .padding(.trailing, 10)
                        
                        Button(action: {
                            CameraLog.log("Torch button tapped")
