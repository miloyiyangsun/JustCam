                        cameraModel.focusAndExpose(at: location, in: geometry.size)
                        
                        // 3ÁßíÂêéÈöêËóèÂØπÁÑ¶ÊåáÁ§∫Âô®
                        DispatchQueue.main.asyncAfter(deadline: .now() + 3) {
                            showFocusIndicator = false
